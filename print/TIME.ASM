.MODEL SMALL
.DATA

MSG1 DB "TIME IS$"
MSG2 DB 10,13,"DATE IS$"

.CODE
MOV AX,@DATA
MOV DS,AX

LEA DX,MSG1
MOV AH,09H
INT 21H
MOV AH,2CH
INT 21H
MOV AL,CH
CALL DISPLAY
MOV AL,CL
CALL DISPLAY

MOV AL,DH
CALL DISPLAY

LEA DX,MSG2
MOV AH,09H
INT 21H

MOV AH,2AH
INT 21H
MOV BX,DX
CALL DISPLAY

MOV AL,BL
CALL DISPLAY

MOV AL,BH
CALL DISPLAY

MOV DL,':'
MOV AH,02H
INT 21H

MOV AX,CX
MOV DX,0
MOV BX,10
DIV BX
MOV CL,DL
MOV DX,0
DIV BX
ADD DL,30H
MOV AH,02H
INT 21H

MOV DL,CL
ADD DL,30H
MOV AH,02H
INT 21H

MOV AH,4CH
INT 21H

DISPLAY PROC
PUSH AX
MOV DL,':'
MOV AH,02H
INT 21H
POP AX
AAM
MOV DX,AX
ADD DX,3030H
XCHG DH,DL
MOV AH,02H
INT 21H
MOV DL,DH
MOV AH,02H
INT 21H
RET
DISPLAY ENDP
END
